    private void btn_actualizar_productosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_actualizar_productosActionPerformed
        if ("".equals(txt_id_productos.getText())) {
            JOptionPane.showMessageDialog(null, "Seleccione una fila");
        } else {
            if (!"".equals(txt_codigo_productos.getText()) || !"".equals(txt_descripcion_productos.getText()) || !"".equals(combobox_proveedor_productos.getSelectedItem()) || !"".equals(txt_cantidad_productos.getText()) || !"".equals(txt_precio_productos.getText())) {
                pdr.setCodigo(txt_codigo_productos.getText());
                pdr.setNombre(txt_descripcion_productos.getText());
                pdr.setProveedor(combobox_proveedor_productos.getSelectedItem().toString());
                pdr.setStock(Integer.parseInt(txt_cantidad_productos.getText()));
                pdr.setPrecio(Double.parseDouble(txt_precio_productos.getText()));
                produdao.modificar_Producto(pdr);
                limpiar_Table();
                listarProductos();
                limpiar_Productos();
                
                JOptionPane.showMessageDialog(null, "Producto modificado con exito");
            } else {
                JOptionPane.showMessageDialog(null, "Los campos estan vacios no puedes modificarlos");
            }
        }
// TODO add your handling code here:
    }//GEN-LAST:event_btn_actualizar_productosActionPerformed

    private void btn_eliminar_productosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminar_productosActionPerformed
        if (!"".equals(txt_id_productos.getText())) {
            int pregunta = JOptionPane.showConfirmDialog(null, "Esta seguro que desea eliminar el Producto");
            if (pregunta == 0) {
                int id = Integer.parseInt(txt_id_productos.getText());
                produdao.eliminar_Producto(id);
                JOptionPane.showMessageDialog(null, "Producto eliminado con exito");
                limpiar_Table();
                limpiar_Productos();
                listarProductos();

            } else {
                JOptionPane.showMessageDialog(null, "ERROR... Seleccione una fila");
            }
        }
// TODO add your handling code here:
    }//GEN-LAST:event_btn_eliminar_productosActionPerformed

